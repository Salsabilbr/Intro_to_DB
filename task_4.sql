iUSE alx_book_store;  
  
SELECT  
  COLUMN_NAME,  
  DATA_TYPE,  
  CHARACTER_MAXIMUM_LENGTH,  
  NUMERIC_PRECISION,  
  NUMERIC_SCALE,  
  COLUMN_DEFAULT,  
  IS_NULLABLE,  
  COLUMN_KEY,  
  EXTRA,
  COLUMN_TYPE,
  TABLE_NAME = 'Books'
  
FROM  
  INFORMATION_SCHEMA.COLUMNS  
WHERE  
  TABLE_NAME = 'Books' AND TABLE_SCHEMA = 'alx_book_store';
